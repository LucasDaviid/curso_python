# Aspas simples
print('Lucas David')

# Aspas duplas
print("Lucas David")

# Escape
print("Lucas \"David\"")  # \ Ignora o caractere a frente

print('Lucas "David"')  # Podemos alternar as aspas para que sejam exibidas sem a utilização de escape
print("Lucas 'David'")  

# r
print(r"Lucas \"David\"")  # r exibe os caracteres de escape, utilizado para expressões regulares
