# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número positivo ou negativo.
# int sem sinal é considerado positivo.

print(11, -11, 0) # int

# float -> Número com ponto flutuante
# O tipo float representa qualquer número positivo ou negativo.
# float sem sinal é considerado positivo.

print(1.7, -25.5, 0.0) # float

# type retorna o tipo que o Python inferiu ao valor.

print( type('Lucas'), type(25), type(-0.7) )